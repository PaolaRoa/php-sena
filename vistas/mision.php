<p> Impartir a la comunidad educativa una formación integral que propicie la relación equilibrada entre la fortaleza de espíritu y la sensibilidad humana, para que en estrecha relación con la permanente construcción del conocimiento y una visión intercultural a través del dominio del inglés y del francés: garantice la educación de los niños, niñas y jóvenes quienes actuarán en beneficio propio, de su entorno y de la sociedad.</p>
